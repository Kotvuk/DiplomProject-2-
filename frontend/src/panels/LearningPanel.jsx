export default function LearningPanel() {
  const { t } = useLang();
  const { theme } = useTheme();
  const [expanded, setExpanded] = useState(null);

  var card = { background: theme.cardBg, border: '1px solid ' + theme.border, borderRadius: 12, padding: 20, cursor: 'pointer', transition: 'all 0.2s' };

  return (
    <div>
      <div style={{ textAlign: 'center', marginBottom: 24 }}>
        <span style={{ display: 'inline-block', background: theme.greenBg, color: theme.green, padding: '6px 16px', borderRadius: 20, fontSize: 13, fontWeight: 600, marginBottom: 12 }}>
          🎓 {t('free')}
        </span>
        <h2 style={{ color: theme.text, fontSize: 22, marginBottom: 6 }}>{t('learningTitle')}</h2>
        <p style={{ color: theme.textMuted, fontSize: 14 }}>{t('learningSubtitle')}</p>
      </div>
      <div style={{ display: 'grid', gridTemplateColumns: 'repeat(auto-fit, minmax(300px, 1fr))', gap: 12 }}>
        {lessons.map((lesson, i) => (
          <motion.div key={i} style={{ ...card, background: expanded === i ? theme.inputBg : theme.cardBg }}
            onClick={() => setExpanded(expanded === i ? null : i)}
            initial={{ opacity: 0, y: 12 }} animate={{ opacity: 1, y: 0 }}
            transition={{ delay: i * 0.03 }}
            whileHover={{ scale: 1.01 }}>
            <div style={{ display: 'flex', alignItems: 'center', gap: 12 }}>
              <span style={{ fontSize: 28 }}>{lesson.icon}</span>
              <div style={{ flex: 1 }}>
                <div style={{ color: theme.text, fontWeight: 600, fontSize: 15 }}>{t(lesson.key)}</div>
                <div style={{ color: theme.textMuted, fontSize: 12, marginTop: 2 }}>{expanded === i ? '▲' : '▼'}</div>
              </div>
            </div>
            <AnimatePresence>
              {expanded === i && (
                <motion.div
                  initial={{ opacity: 0, height: 0 }}
                  animate={{ opacity: 1, height: 'auto' }}
                  exit={{ opacity: 0, height: 0 }}
                  style={{ marginTop: 16, color: theme.textSecondary, fontSize: 13, lineHeight: 1.8, whiteSpace: 'pre-wrap', borderTop: '1px solid ' + theme.border, paddingTop: 16, overflow: 'hidden' }}>
                  {lesson.content}
                </motion.div>
              )}
            </AnimatePresence>
          </motion.div>
        ))}
      </div>
    </div>
  );
}
